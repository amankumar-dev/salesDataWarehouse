# Creating order table
def order_table():
    with conn:
        try:
            cursor.execute('''CREATE TABLE IF NOT EXISTS order_table(
                                orderId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                                custId INT REFERENCES cust_table(custId) ON DELETE CASCADE ON UPDATE CASCADE,
                                prodId INT REFERENCES prod_table(prodId) ON DELETE CASCADE ON UPDATE CASCADE,
                                storeId INT REFERENCES store_table(storeId) ON DELETE CASCADE ON UPDATE CASCADE,
                                quantity INT NOT NULL,
                                unitprice INT NOT NULL,
                                costprice INT NOT NULL,
                                revenue INT NOT NULL,
                                costamount INT NOT NULL,
                                profit INT NOT NULL,
                                order_date TIMESTAMPTZ);''')
            print('order table created')
        except Exception as e:
            print('order table is not created ',e)

