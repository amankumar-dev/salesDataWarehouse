from db import conn,cursor
from etl import custDf,prodDf,storeDf,salesDf,factDf
from psycopg2.extras import execute_values

# creating customer table
def cust_table():
    with conn: 
        try:
            cursor.execute('''CREATE TABLE IF NOT EXISTS cust_table(
                            custId INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            custName VARCHAR(20) NOT NULL,
                            email VARCHAR(30) NOT NULL,
                            city VARCHAR(10) NOT NULL,
                            source_custId INT);''')
            print('table created')
        except Exception as e:
            print("Table can't created ",e)

# inserting value in customer table
def insert_cust():
    query='''INSERT INTO cust_table(custname,email,city,source_custId) VALUES %s'''
    values=list(custDf.itertuples(index=False,name=None))
    with conn:
        try:
            execute_values(cursor,query,values)
            print('value inserted')
        except Exception as e:
            print("Value can't inserted",e)

